using UnityEngine;
using System.Collections;

public class Rotator : MonoBehaviour
{
	//-------------------------------------------------------------------------------------------------------------------------
	public	Vector3			RotationSpeed;

	//-------------------------------------------------------------------------------------------------------------------------
	public void Awake()
	{
	}

	//-------------------------------------------------------------------------------------------------------------------------
	public void Start()
	{
	}

	//-------------------------------------------------------------------------------------------------------------------------
	public void Update()
	{
		transform.rotation = transform.rotation * Quaternion.Euler( ( RotationSpeed * Time.deltaTime ) );
	}


}

